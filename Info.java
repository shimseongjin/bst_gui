
public class Info {
	public String name;
	public String sex;
	public int age;
	public int id;
	public String phonenumber;
	public int grade;
	public double score;
	public Info(String name, String sex, int age, int id, String phonenumber, int grade, double score)
	{
		this.name=name;
		this.sex=sex;
		this.age=age;
		this.id=id;
		this.phonenumber=phonenumber;
		this.grade=grade;
		this.score=score;
	}
	public Info(){}
}
